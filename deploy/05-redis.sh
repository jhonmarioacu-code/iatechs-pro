#!/usr/bin/env bash

set -Eeuo pipefail

################################################################################
# IAtechsPro Enterprise Installer
# File: 05-redis.sh
# Purpose:
#   Redis Server
#   Laravel Cache
#   Laravel Queue
#   Laravel Session
#   Horizon
#
# AWS EC2 Ubuntu 24.04
################################################################################

echo ""
echo "========================================================="
echo " IAtechsPro Enterprise Installer"
echo " Step 05 - Redis"
echo "========================================================="
echo ""

################################################################################
# ROOT VALIDATION
################################################################################

if [[ $EUID -ne 0 ]]; then
    echo "ERROR: Execute as root."
    echo ""
    echo "sudo bash deploy/05-redis.sh"
    exit 1
fi

################################################################################
# INSTALL REDIS
################################################################################

echo ""
echo "========================================================="
echo "Installing Redis..."
echo "========================================================="
echo ""

apt-get update -y

apt-get install -y redis-server

################################################################################
# ENABLE REDIS
################################################################################

systemctl enable redis-server
systemctl start redis-server

################################################################################
# REDIS CONFIG
################################################################################

echo ""
echo "========================================================="
echo "Configuring Redis..."
echo "========================================================="
echo ""

REDIS_CONF="/etc/redis/redis.conf"

cp "${REDIS_CONF}" "${REDIS_CONF}.backup"

################################################################################
# SECURITY
################################################################################

sed -i "s/^bind .*/bind 127.0.0.1 ::1/" ${REDIS_CONF}

sed -i "s/^protected-mode .*/protected-mode yes/" ${REDIS_CONF}

################################################################################
# MEMORY
################################################################################

cat >> ${REDIS_CONF} <<EOF

################################################################################
# IAtechsPro Optimization
################################################################################

maxmemory 512mb

maxmemory-policy allkeys-lru

tcp-keepalive 60

timeout 0

appendonly yes

appendfsync everysec

################################################################################
EOF

################################################################################
# RESTART REDIS
################################################################################

systemctl restart redis-server

################################################################################
# HEALTH CHECK
################################################################################

echo ""
echo "========================================================="
echo "Testing Redis..."
echo "========================================================="
echo ""

redis-cli ping

################################################################################
# STATUS
################################################################################

echo ""
echo "========================================================="
echo "Redis Information"
echo "========================================================="
echo ""

redis-cli INFO memory | grep used_memory_human

################################################################################
# LARAVEL CONFIG
################################################################################

echo ""
echo "========================================================="
echo "Laravel .env Configuration"
echo "========================================================="
echo ""

cat <<EOF

CACHE_STORE=redis

QUEUE_CONNECTION=redis

SESSION_DRIVER=redis

REDIS_CLIENT=phpredis

REDIS_HOST=127.0.0.1

REDIS_PASSWORD=null

REDIS_PORT=6379

EOF

################################################################################
# FINISH
################################################################################

echo ""
echo "========================================================="
echo "Step 05 Completed Successfully"
echo "========================================================="
echo ""

echo "Installed:"
echo ""

echo "✓ Redis Server"
echo "✓ Redis Persistence"
echo "✓ Laravel Cache"
echo "✓ Laravel Queue"
echo "✓ Laravel Session"
echo "✓ Horizon Ready"

echo ""
echo "Redis Test:"
echo ""

redis-cli ping

echo ""
echo "Expected:"
echo "PONG"

echo ""
echo "Next Step:"
echo ""

echo "sudo bash deploy/06-nginx.sh"
echo ""